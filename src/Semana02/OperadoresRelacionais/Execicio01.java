package Semana02.OperadoresRelacionais;

public class Execicio01 {
    public static void main(String[] args) {
        //Inicializar uma variável int saldo = 500;
        // e aplicar o operador de atribuição de incremento += e depois exibir o saldo atualizado.
        int saldo = 500;
        saldo += 100;
        System.out.println("O saldo final é: "+ saldo);
    }
}
