package Semana02.EstruturasdeRepeticao;

public class Execicio01 {
    public static void main(String[] args) {
        //Solicitar ao usuário um número e imprimir
        //todos os números de 1 até o número fornecido (inclusive).
        int numero = 1;
        while (numero <= 100){
            System.out.println(numero);
            numero++;
        }
    }
}
