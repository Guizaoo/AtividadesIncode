package Semana02.EstruturasCondicionais;

public class Execicio01 {
    public static void main(String[] args) {
        //Solicitar ao usuário um número e verificar se ele é positivo, negativo ou zero.
        int numero = 0;
        if (numero == 1){
            System.out.println("Ele é positivo");
        }else if (numero <= 1){
            System.out.println("Ele é negativo");
        }else {
            System.out.println("Ele é zero");
        }
    }
}
