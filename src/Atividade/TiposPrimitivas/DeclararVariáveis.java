package Atividade.TiposPrimitivas;

public class DeclararVariáveis {
    //Declarar variáveis dos
    // tipos byte, short, int, long, float, double, char e boolean, exibindo seus valores.
    public static void main(String[] args) {
        byte x = -12 ;
        short s = 1;
        int inteiro = 90;
        long n = 100000;
        float k = 1.2f;
        char Genero = 'M';
        boolean correr = true;
        System.out.println("variável byte " + x );
        System.out.println("variável short " + s );
        System.out.println("variável int " + inteiro );
        System.out.println("variável long " + n );
        System.out.println("variável float " + k );
        System.out.println("variável char " + Genero );
        System.out.println("variável boolean " + correr );

    }
}
